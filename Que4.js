//Common methods of strings in javascript

let a = "Hello I am a Full Stack Developer";

console.log(a.length) //gives the length of a string
console.log(a.charAt(2)) // gives the character which is at position 2 in a string
console.log(a.toUpperCase()) // all characters in a string will be in uppercase
console.log(a.toLowerCase()) // all characters in a string will be in lowercase
console.log(a.concat());// concatenation of a string with other string

